#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	len;

	i = 0;
	len = ft_strlen(src);
	if (!dstsize)
		return (len);
	while (dstsize && src[i])
	{
		dst[i] = src[i];
		dstsize--;
		i++;
	}
	dst[i] = '\0';
	return (len);
}
